namespace DatevBridge.UI.Theme
{
    /// <summary>
    /// Layout constants for consistent spacing and sizing across all UI elements.
    /// Based on an 8px base unit for visual harmony.
    /// </summary>
    public static class LayoutConstants
    {
        // ========== SPACING SCALE ==========

        /// <summary>Extra small spacing (4px) - tight spacing between related items</summary>
        public const int SpaceXS = 4;

        /// <summary>Small spacing (8px) - default spacing within groups</summary>
        public const int SpaceSM = 8;

        /// <summary>Medium spacing (16px) - spacing between groups</summary>
        public const int SpaceMD = 16;

        /// <summary>Large spacing (24px) - spacing between sections</summary>
        public const int SpaceLG = 24;

        // ========== CONTROL DIMENSIONS ==========

        /// <summary>Standard button height (32px)</summary>
        public const int ButtonHeight = 32;

        /// <summary>Card internal padding (12px)</summary>
        public const int CardPadding = 12;
    }
}
